//綁定計算Wild個數的方法
func (t *Mode) WildCount(lott [15]int, freeid int) int {

	ReturnCount := 0

	for i := range lott {
		if lott[i] == freeid {
			ReturnCount++
		}
	}

	return ReturnCount
}

